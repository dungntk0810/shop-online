package entity;

import java.util.Date;

/**
 *
 * @author mylov
 */
public class Statistical {
    private Date date;
    private float amount;

    public Statistical() {
    }

    public Statistical(Date date, float amount) {
        this.date = date;
        this.amount = amount;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public float getAmount() {
        return amount;
    }

    public void setAmount(float amount) {
        this.amount = amount;
    }
    
}
